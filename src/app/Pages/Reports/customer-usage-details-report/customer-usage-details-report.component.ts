  convertInExcel() {
    var arry1: any = [];
    var obj1: any = new Object();
    if (this.excelData.length > 0) {
      for (var i = 0; i < this.excelData.length; i++) {

        obj1['Transaction Date'] = this.excelData[i]['TRANSACTION_DATE']
          ? this.datepipe.transform(this.excelData[i]['TRANSACTION_DATE'], 'dd/MM/yyyy hh:mm a')
          : '-';
        obj1['Wallet Amount'] = this.excelData[i]['WALLET_AMOUNT']
          ? this.excelData[i]['WALLET_AMOUNT']
          : '-';
        obj1['Remark'] = this.excelData[i]['REMARK']
          ? this.excelData[i]['REMARK']
          : '-';
        arry1.push(Object.assign({}, obj1));
        if (i == this.excelData.length - 1) {
          this._exportService.exportExcel(
            arry1,
            'Customer Usage Details Report ' +
            this.datepipe.transform(new Date(), 'dd/MM/yyyy')
          );
        }
      }
    } else {
      this.message.error('There is a No Data', '');
    }
  }
